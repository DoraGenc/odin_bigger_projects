class StrawberryCat

  attr_reader :mood, :hp

  def initialize
    @mood = 0
    @hp = 7
  end


  def change_mood(value)

    value = value.to_i
    @mood += value

    if @mood > 3 
      @mood = 3
    elsif @mood < -3
      @mood = -3
    end
  end

